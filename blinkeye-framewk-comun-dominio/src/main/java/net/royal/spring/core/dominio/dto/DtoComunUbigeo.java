package net.royal.spring.core.dominio.dto;

public class DtoComunUbigeo {
	private String pais;
	private String departamento;
	private String provincia;
	private String zonapostal;
	
	private String descripcion;
	private String codigoelemento;

	private String departamentoNombre;
	private String provinciaNombre;
	private String zonapostalNombre;
	
	public String getPais() {
		return pais;
	}
	public void setPais(String pais) {
		this.pais = pais;
	}
	public String getDepartamento() {
		return departamento;
	}
	public void setDepartamento(String departamento) {
		this.departamento = departamento;
	}
	public String getProvincia() {
		return provincia;
	}
	public void setProvincia(String provincia) {
		this.provincia = provincia;
	}
	public String getZonapostal() {
		return zonapostal;
	}
	public void setZonapostal(String zonapostal) {
		this.zonapostal = zonapostal;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public String getCodigoelemento() {
		return codigoelemento;
	}
	public void setCodigoelemento(String codigoelemento) {
		this.codigoelemento = codigoelemento;
	}
	public String getDepartamentoNombre() {
		return departamentoNombre;
	}
	public void setDepartamentoNombre(String departamentoNombre) {
		this.departamentoNombre = departamentoNombre;
	}
	public String getProvinciaNombre() {
		return provinciaNombre;
	}
	public void setProvinciaNombre(String provinciaNombre) {
		this.provinciaNombre = provinciaNombre;
	}
	public String getZonapostalNombre() {
		return zonapostalNombre;
	}
	public void setZonapostalNombre(String zonapostalNombre) {
		this.zonapostalNombre = zonapostalNombre;
	}	
}
