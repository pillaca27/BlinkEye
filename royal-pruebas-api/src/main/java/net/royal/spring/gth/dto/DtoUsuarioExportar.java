package net.royal.spring.gth.dto;

public class DtoUsuarioExportar {
	
	private String usuario;
	private String nombre;
	private String tipoPlanillaNombre;
	private String compania;
	private String companiaNombre;

	public String getUsuario() {
		return usuario;
	}

	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public String getTipoPlanillaNombre() {
		return tipoPlanillaNombre;
	}

	public void setTipoPlanillaNombre(String tipoPlanillaNombre) {
		this.tipoPlanillaNombre = tipoPlanillaNombre;
	}

	public String getCompania() {
		return compania;
	}

	public void setCompania(String compania) {
		this.compania = compania;
	}

	public String getCompaniaNombre() {
		return companiaNombre;
	}

	public void setCompaniaNombre(String companiaNombre) {
		this.companiaNombre = companiaNombre;
	}

}
